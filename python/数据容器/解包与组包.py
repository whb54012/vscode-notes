"""
Python 解包 (Unpacking) 与组包 (Packing) —— 各数据容器支持一览
===============================================================

核心运算符:
  *    → 适用于所有可迭代对象 (list, tuple, str, set, dict, range, 生成器等)
  **   → 仅适用于 dict

====================================================================
一、基础解包 (所有可迭代对象都支持)
====================================================================

列表:  a, b, c = [1, 2, 3]
元组:  a, b, c = (1, 2, 3)
字符串: a, b, c = "abc"          # a='a', b='b', c='c'
集合:  a, b, c = {4, 5, 6}      # 无序，顺序不确定
字典:  a, b = {"x": 1, "y": 2}  # 解出的是 key: a='x', b='y'
range: a, b, c = range(3)       # a=0, b=1, c=2
生成器: a, b = (x for x in [7, 8])  # a=7, b=8

====================================================================
二、* 解包 (函数调用时展开)
====================================================================

print(*[1, 2, 3])       # 等价于 print(1, 2, 3)
func(*[1, 2, 3])        # 等价于 func(1, 2, 3)

支持列表/元组/字符串/集合/字典(range)/生成器…… 所有可迭代对象

====================================================================
三、** 解包 (仅 dict → 展开为关键字参数)
====================================================================

d = {"a": 1, "b": 2}
func(**d)               # 等价于 func(a=1, b=2)

合并字典:
d3 = {**d1, **d2}       # 仅 dict 支持

====================================================================
四、* 组包 (剩余元素打包成 list)
====================================================================

a, *b = [1, 2, 3, 4]        # a=1,  b=[2, 3, 4]          ← 列表
a, *b = (1, 2, 3, 4)        # a=1,  b=[2, 3, 4]          ← 元组
a, *b = "hello"              # a='h', b=['e', 'l', 'l', 'o']  ← 字符串
*a, b = range(5)             # a=[0,1,2,3],  b=4          ← range
a, *mid, z = [1,2,3,4,5]    # a=1, mid=[2,3,4], z=5

无论原容器是什么类型，* 组包结果总是 list

====================================================================
五、* / ** 在字面量中展开 (组建新容器)
====================================================================

[1, *[2, 3], 4]             # 列表内展开 → [1, 2, 3, 4]
(1, *(2, 3), 4)             # 元组内展开 → (1, 2, 3, 4)
{1, *{2, 3}, 4}             # 集合内展开 → {1, 2, 3, 4}
[*"abc"]                     # 字符串展开为列表 → ['a', 'b', 'c']
{**{"a": 1}, "b": 2}        # 字典展开 → {'a': 1, 'b': 2}

====================================================================
六、跨容器举例
====================================================================

# 1. 集合解包赋值
a, b, c = {1, 2, 3}

# 2. range 解包
first, *rest = range(10)    # first=0, rest=[1..9]

# 3. 生成器解包
gen = (x**2 for x in range(3))
a, b, c = gen               # a=0, b=1, c=4

# 4. 嵌套解包 (任意容器混合)
data = [1, (2, 3), "ab"]
a, (b, c), (d, e) = data   # a=1, b=2, c=3, d='a', e='b'

# 5. bytes / bytearray
b1, b2, b3 = b"abc"         # b1=97, b2=98, b3=99

# 6. dict 解包键
k1, k2 = {"name": "Alice", "age": 20}

# 7. dict 解包值（借助 .values()）
v1, v2 = {"name": "Alice", "age": 20}.values()

# 8. dict 解包键值对（借助 .items()）
(k1, v1), (k2, v2) = {"name": "Alice", "age": 20}.items()

# 9.array.array
import array
a1, a2, a3 = array.array('i', [10, 20, 30])

====================================================================
七、总结
====================================================================

容器类型         *拆包/传参   **拆包    *组包(剩余)    **组包(kwargs)
──────────────  ──────────  ──────  ─────────────  ──────────────
list               ✅         ❌       ✅→list          ❌
tuple              ✅         ❌       ✅→list          ❌
str                ✅         ❌       ✅→list          ❌
bytes/bytearray    ✅         ❌       ✅→list          ❌
set/frozenset      ✅         ❌       ✅→list          ❌
dict               ✅(键)     ✅       ✅→list          ✅→dict
range              ✅         ❌       ✅→list          ❌
生成器/迭代器      ✅         ❌       ✅→list          ❌
array.array        ✅         ❌       ✅→list          ❌
numpy.ndarray      ✅         ❌       ✅→list          ❌
自定义可迭代类      ✅         ❌       ✅→list          ❌

规则:
  单星 *   → 适用于 一切可迭代对象
  双星 **  → 只适用于 字典
  * 组包   → 结果永远是 list
  ** 组包  → 结果永远是 dict
"""
